export function rollingBacktestMarkdown(report) {
    const evaluationLabel = (value) => typeof value === 'number' ? `${value}日` : value ?? '固定';
    const rateLabel = (value) => value == null ? '待样本' : `${value}%`;
    const actionLabels = {
        full_exit: '清仓提示',
        defense_reduce: '防守减仓',
        range_high: '区间高抛',
        range_low: '区间低吸',
        trend_take_profit: '趋势止盈',
        trend_entry: '趋势回踩买入',
        reentry: '卖后回补',
    };
    const lines = [
        `# ${report.as_of.slice(0, 10)} 近${report.window_days}日滚动买卖点回测`,
        '',
        `- 标的：${report.universe.completed}/${report.universe.requested}`,
        `- ${report.universe.intraday_period}覆盖门槛：至少 ${report.universe.minimum_trading_days} 个交易日`,
        `- 覆盖不足：${report.universe.coverage_issues.length} 只`,
        `- 证据等级：${report.research_only ? '扩展研究层（不参与晋级）' : '严格验证层'}`,
        `- 候选策略：${report.decision_policy.id}`,
        `- 活动基线：${report.active_baseline.decision_policy.id}`,
        `- 方向正确口径：${report.evaluation_contract.direction_correct}`,
        `- 反事实新买点去重：至少间隔 ${report.evaluation_contract.counterfactual_entry_cooldown_trading_days} 个交易日`,
        '- 做T最低毛价差：股票/ETF 3%，可转债 1%',
        '- 高抛低吸最低毛价差：股票/ETF 2%，可转债 0.5%',
        `- 可评估信号：${report.metrics.objective.samples}`,
        `- 固定场景周期准确率：${rateLabel(report.metrics.objective.accuracy_pct)}`,
        `- 同资产同日同策略聚类准确率：${rateLabel(report.metrics.clustered_objective.accuracy_pct)}（${report.metrics.clustered_objective.correct}/${report.metrics.clustered_objective.samples} 个相关性调整后样本）`,
        `- 全样本信号组合收益代理：${report.performance.objective_overall.candidate.total_return_pct}%`,
        `- 全样本信号组合最大回撤代理：${(report.performance.objective_overall.candidate.max_drawdown_ratio * 100).toFixed(2)}%`,
        `- 全样本信号组合利润因子：${report.performance.objective_overall.candidate.profit_factor ?? '无亏损样本'}`,
        `- 历史样本外准确率：${rateLabel(report.metrics.objective_out_of_sample.accuracy_pct)}`,
        `- 历史样本外95%置信下界：${rateLabel(report.metrics.objective_out_of_sample.confidence_lower_bound_pct)}`,
        `- 未来影子天数：${report.promotion.shadow_days}`,
        `- 未来影子置信下界：${rateLabel(report.promotion.shadow_metrics.confidence_lower_bound_pct)}`,
        `- 80%目标：${report.research_only ? '仅作扩展研究，不参与晋级' : report.promotion.ready ? '全部硬门槛已达到' : '未达到，继续收集未来证据'}`,
        '',
        '## 固定场景评价',
        '',
        ...Object.entries(report.metrics.objective_scenarios)
            .map(([name, item]) => `- ${name}：${rateLabel(item.accuracy_pct)}（${evaluationLabel(item.evaluation_horizon)}评价，全部样本 ${item.correct}/${item.samples}）`),
        `- 真实做T闭环：${report.metrics.objective_t_trade_pairs.length} 组；只统计“高抛意图卖出 → 做T接回”，不把风险减仓算成做T。`,
        `- 风险减仓后低位接回：${report.metrics.objective_risk_recovery_pairs.length} 组，单独记录。`,
        `- 待闭合真实做T卖出腿：${report.metrics.objective_position_cycle_ledger.open_t_sell_legs} 个。`,
        `- 避免卖飞拆解：及时卖出 ${report.metrics.objective_scenarios.avoid_sell_flying.breakdown.timely_sells}，保留核心仓且3日方向正确 ${report.metrics.objective_scenarios.avoid_sell_flying.breakdown.core_preserved_directional_sells}，真正错误卖出 ${report.metrics.objective_scenarios.avoid_sell_flying.breakdown.wrong_sells}。`,
        '',
        '## 独立场景案例库',
        '',
        `- 原始场景 case：${report.case_library.raw.samples}`,
        `- 间隔至少 ${report.case_library.sampling_contract.independent_cooldown_trading_days} 个交易日后的独立 case：${report.case_library.independent.samples}`,
        ...Object.entries(report.case_library.independent.action_metrics)
            .map(([name, item]) => `- ${actionLabels[name] ?? name}：${rateLabel(item.accuracy_pct)}（${evaluationLabel(item.evaluation_horizon)}评价，独立样本 ${item.correct}/${item.samples}）`),
        `- 高抛低吸场景闭环：原始 ${report.case_library.raw.high_low_pairs.length} 组，去相关后 ${report.case_library.independent.high_low_pairs.length} 组；这是独立场景回放，不冒充真实持仓做T。`,
        `- 严格做T场景闭环：去相关后 ${report.case_library.independent.position_cycle_ledger.t_pairs.length} 组。`,
        `- 待闭合高抛低吸：高抛腿 ${report.case_library.independent.high_low_cycle_ledger.open_sell_legs} 个，未匹配低吸腿 ${report.case_library.independent.high_low_cycle_ledger.unmatched_buy_legs} 个。`,
        `- 拒绝抄底负样本：${report.case_library.bottom_fishing_abstentions.correct_abstentions}/${report.case_library.bottom_fishing_abstentions.samples} 次拦截正确。`,
        '- 场景案例库用于发现弱项，不参与账户收益代理，也不直接作为策略晋级证据。',
        '',
        '## 历史样本外评价',
        '',
        ...Object.entries(report.metrics.objective_out_of_sample_scenarios)
            .map(([name, item]) => `- ${name}：${rateLabel(item.accuracy_pct)}（${evaluationLabel(item.evaluation_horizon)}评价，样本外 ${item.correct}/${item.samples}）`),
        '',
        '## 卖点时机拆解',
        '',
        `- 可同时评价次日和3日走势：${report.metrics.sell_timing.samples}`,
        `- 及时卖出：${report.metrics.sell_timing.timely}`,
        `- 方向正确但卖早：${report.metrics.sell_timing.early_but_directionally_correct}`,
        `- 方向错误：${report.metrics.sell_timing.wrong_direction}`,
        `- 3日方向准确率：${report.metrics.sell_timing.directional_accuracy_pct ?? '待样本'}%`,
        `- 次日时机准确率：${report.metrics.sell_timing.timing_accuracy_pct ?? '待样本'}%`,
        '',
        '## 抄底确认漏斗',
        '',
        `- 下跌趋势交易日：${report.signal_audit.diagnostics.bottom_entry_filter_funnel.downtrend_days}`,
        `- 出现盘中反转：${report.signal_audit.diagnostics.bottom_entry_filter_funnel.actionable_reversal_days}`,
        `- 5m与15m共同确认：${report.signal_audit.diagnostics.bottom_entry_filter_funnel.multi_period_confirmed_days}`,
        `- 同时满足日线修复：${report.signal_audit.diagnostics.bottom_entry_filter_funnel.daily_repair_confirmed_days}`,
        '- 未同时满足全部条件时只观察，不为补齐场景样本强行抄底。',
        '',
        '## 约束',
        '',
        '- 评价周期在查看单个结果前固定，禁止为某个信号挑选最有利周期。',
        '- 买入后上涨、卖出后下跌即记为方向正确；做T和高抛低吸分别执行上述资产分层毛价差门槛。',
        '- 只使用信号时点之前的日线和当日已闭合分钟K，不使用未来数据生成信号。',
        `- 时间切分是否是真正样本外：${report.evaluation_contract.temporal_holdout_is_true_out_of_sample ? '是' : '否；标的名单不是在回测起点按当时数据冻结，时间尾段仍受期末选样污染。'}`,
        `- 价格尺度跳变剔除：${report.data_quality.invalid_outcomes} 个评价结果。`,
        '- 收益、回撤和利润因子是按信号方向及动作仓位构造的组合代理，不等同真实账户收益。',
        '- 回测结果只生成策略候选；未来影子样本和风险门槛未通过前不升级活动策略。',
    ];
    return lines.join('\n');
}
